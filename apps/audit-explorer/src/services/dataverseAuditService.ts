import type {
  AttributeChange,
  AuditEvent,
  AuditOperation,
  AuditedTable,
} from '../types/audit'
import { mockAuditService } from './mockAuditService'
import { powerModeReady } from '../PowerProvider'
import { AuditsService } from '../generated/services/AuditsService'
import { RetrieveAuditDetailsService } from '../generated/services/RetrieveAuditDetailsService'
import type { Audits } from '../generated/models/AuditsModel'

/**
 * Real implementation of {@link AuditService} backed by the Dataverse `audit`
 * table and the `RetrieveAuditDetails` function. It auto-falls back to the
 * mock service when running outside a Power Platform host, so local
 * `npm run dev` straight against Vite still produces a usable dashboard.
 *
 * Mode gating: every method awaits {@link powerModeReady} before touching the
 * generated client. Outside the host the SDK's connection lookup stalls
 * indefinitely instead of throwing, so a plain try/catch around the call
 * isn't enough — we have to short-circuit to mock when the provider has
 * resolved to `local-mock`.
 *
 * Generated by:
 *   pac code add-data-source -a dataverse -t audit
 *   power-apps add-dataverse-api --api-name RetrieveAuditDetails
 *
 * If those generators are re-run with different shapes, update the SELECT_FIELDS
 * list and the mappers below.
 */

const OPERATION_BY_CODE: Record<number, AuditOperation> = {
  1: 'Create',
  2: 'Update',
  3: 'Delete',
  4: 'Access',
}

function initialsFrom(name: string): string {
  const parts = name.trim().split(/\s+/)
  const letters = (parts[0]?.[0] ?? '') + (parts[1]?.[0] ?? parts[0]?.[1] ?? '')
  return letters.toUpperCase() || '??'
}

/**
 * Only real Web API columns may appear in `$select`. The `...name` properties
 * in the generated model (objecttypecodename, useridname, objectidname) are
 * SDK-side virtual attributes — selecting them yields 0x80060888 "Could not
 * find a property named ... on type Microsoft.Dynamics.CRM.audit". Their
 * display values arrive as OData formatted-value annotations instead.
 */
const SELECT_FIELDS = [
  'auditid',
  'createdon',
  'operation',
  'objecttypecode',
  '_objectid_value',
  '_userid_value',
]

/** OData annotation suffix carrying option-set / lookup display labels. */
const FV = '@OData.Community.Display.V1.FormattedValue'

/** Read the formatted-value annotation for a column, if present. */
function formatted(row: Record<string, unknown>, column: string): string | undefined {
  const value = row[`${column}${FV}`]
  return typeof value === 'string' && value !== '' ? value : undefined
}

function toAuditEvent(raw: Audits): AuditEvent {
  // Annotations aren't part of the generated model, so widen for access.
  const row = raw as Audits & Record<string, unknown>
  const opCode =
    typeof raw.operation === 'number' ? raw.operation : Number(raw.operation)
  const userName =
    formatted(row, '_userid_value') ?? raw.useridname ?? 'Unknown'
  const logical = raw.objecttypecode ?? ''
  return {
    id: raw.auditid,
    createdOn: raw.createdon,
    operation: OPERATION_BY_CODE[opCode] ?? 'Update',
    tableName:
      formatted(row, 'objecttypecode') ?? raw.objecttypecodename ?? logical,
    tableLogicalName: logical,
    recordId: raw._objectid_value ?? '',
    recordName:
      formatted(row, '_objectid_value') ?? raw.objectidname ?? '',
    user: { name: userName, initials: initialsFrom(userName) },
    changes: [],
  }
}

/**
 * Defensive view of a RetrieveAuditDetails response. The function can return
 * an AttributeAuditDetail (the case we care about — column changes),
 * a ChangedAttributesAuditDetail, or a RelationshipAuditDetail. We map
 * OldValue / NewValue keys into a flat change list and ignore everything else.
 */
interface AttributeAuditDetail {
  OldValue?: Record<string, unknown>
  NewValue?: Record<string, unknown>
}
interface RetrieveAuditDetailsResponse {
  AuditDetail?: AttributeAuditDetail
}

function formatAuditValue(value: unknown): string {
  if (value === null || value === undefined) return ''
  if (typeof value === 'object') return JSON.stringify(value)
  return String(value)
}

function isAttributeKey(key: string): boolean {
  // Skip OData annotations (`@odata.type`, `attr@OData.Community...`) and the
  // implicit type discriminator. The remaining keys are attribute logical names.
  return !key.startsWith('@') && !key.includes('@odata.')
}

function mapAuditDetail(detail: AttributeAuditDetail | undefined): AttributeChange[] {
  if (!detail) return []
  const oldVals = detail.OldValue ?? {}
  const newVals = detail.NewValue ?? {}
  const keys = new Set<string>([
    ...Object.keys(oldVals).filter(isAttributeKey),
    ...Object.keys(newVals).filter(isAttributeKey),
  ])
  const changes: AttributeChange[] = []
  for (const key of keys) {
    changes.push({
      attribute: key,
      oldValue: formatAuditValue(oldVals[key]),
      newValue: formatAuditValue(newVals[key]),
    })
  }
  return changes
}

export class DataverseAuditService {
  async list(): Promise<AuditEvent[]> {
    const mode = await powerModeReady
    if (mode !== 'power-platform') return mockAuditService.list()
    try {
      // 5000 = one Dataverse page. The previous 500 cap meant a busy
      // environment filled the whole result set with the current week,
      // leaving the 7/30/all range filter with nothing to distinguish.
      const result = await AuditsService.getAll({
        select: SELECT_FIELDS,
        orderBy: ['createdon desc'],
        top: 5000,
      })
      if (!result.success || !result.data) {
        console.warn('[audit] list() falling back to mock — result:', result)
        return mockAuditService.list()
      }
      console.info('[audit] list() got', result.data.length, 'rows from Dataverse')
      return result.data.map(toAuditEvent)
    } catch (err) {
      console.warn('[audit] list() threw, falling back to mock:', err)
      return mockAuditService.list()
    }
  }

  async getChanges(auditId: string): Promise<AttributeChange[]> {
    const mode = await powerModeReady
    if (mode !== 'power-platform') return mockAuditService.getChanges(auditId)
    try {
      const result =
        await RetrieveAuditDetailsService.RetrieveAuditDetails(auditId)
      if (!result.success || !result.data) {
        console.warn(
          '[audit] getChanges() falling back to mock — result:',
          result,
        )
        return mockAuditService.getChanges(auditId)
      }
      const response = result.data as RetrieveAuditDetailsResponse
      return mapAuditDetail(response.AuditDetail)
    } catch (err) {
      console.warn('[audit] getChanges() threw, falling back to mock:', err)
      return mockAuditService.getChanges(auditId)
    }
  }

  /**
   * Derive the slicer list from the distinct tables present in the audit log.
   *
   * The Power Apps code app data client only exposes per-table metadata
   * (`getEntityMetadata`) and Custom APIs — there is no direct OData query
   * against `EntityDefinitions`, so we can't filter on `IsAuditEnabled` from
   * here. Reading the log itself yields exactly the tables with activity in
   * the selected window, which is what the slicer shows anyway. Tables that
   * are audited but quiet won't appear until they have at least one event.
   */
  async listAuditedTables(): Promise<AuditedTable[]> {
    const mode = await powerModeReady
    if (mode !== 'power-platform') return mockAuditService.listAuditedTables()
    try {
      const result = await AuditsService.getAll({
        select: ['objecttypecode'],
        top: 5000,
      })
      if (!result.success || !result.data) {
        console.warn(
          '[audit] listAuditedTables() falling back to mock — result:',
          result,
        )
        return mockAuditService.listAuditedTables()
      }
      const seen = new Map<string, AuditedTable>()
      for (const raw of result.data) {
        const row = raw as Audits & Record<string, unknown>
        const logical = raw.objecttypecode
        if (!logical || seen.has(logical)) continue
        seen.set(logical, {
          logicalName: logical,
          displayName: formatted(row, 'objecttypecode') ?? logical,
        })
      }
      return [...seen.values()].sort((a, b) =>
        a.displayName.localeCompare(b.displayName),
      )
    } catch (err) {
      console.warn(
        '[audit] listAuditedTables() threw, falling back to mock:',
        err,
      )
      return mockAuditService.listAuditedTables()
    }
  }
}

export const dataverseAuditService = new DataverseAuditService()
